<?php

/**
 * Operadores de incremento e decremento:
 * 
 * ++
 * --
*/


$number = 10;
echo $number;
echo "\n";

// Pré incremento:
echo ++$number;
echo "\n";

// Pós incremento:
echo $number++ . " " . $number;
echo "\n";

// Pré decremento:
echo --$number;
echo "\n";

// Pós decremento:
echo $number-- . " " . $number;
echo "\n";